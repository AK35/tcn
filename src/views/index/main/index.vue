<script>
export default {
  name: 'main',
  data() {
    return {
      autoplay: false,
      leftTopActive: true,
      leftMiddleActive: false,
      leftBottomActive: false,
      rightTopActive: false,
      rightMiddleActive: false,
      rightBottomActive: false
    }
  },
  mounted() {
    var _this = this
    this.timer = setInterval(this.spinTimer, 10000)
  },
  methods: {
    spinClick(type) {
      clearInterval(this.timer)
      this.leftTopActive = false
      this.leftMiddleActive = false
      this.leftBottomActive = false
      this.rightTopActive = false
      this.rightMiddleActive = false
      this.rightBottomActive = false
      if (type === 'leftTop') {
        this.leftTopActive = true
      } else if (type == 'leftMiddle') {
        this.leftMiddleActive = true
      } else if (type == 'leftBottom') {
        this.leftBottomActive = true
      } else if (type == 'rightTop') {
        this.rightTopActive = true
      } else if (type == 'rightMiddle') {
        this.rightMiddleActive = true
      } else if (type == 'rightBottom') {
        this.rightBottomActive = true
      }
      this.timer = setInterval(this.spinTimer, 10000)
    },
    spinTimer() {
      if (this.leftTopActive) {
        this.leftTopActive = false
        this.leftMiddleActive = true
      } else if (this.leftMiddleActive) {
        this.leftMiddleActive = false
        this.leftBottomActive = true
      } else if (this.leftBottomActive) {
        this.leftBottomActive = false
        this.rightTopActive = true
      } else if (this.rightTopActive) {
        this.rightTopActive = false
        this.rightMiddleActive = true
      } else if (this.rightMiddleActive) {
        this.rightMiddleActive = false
        this.rightBottomActive = true
      } else if (this.rightBottomActive) {
        this.rightBottomActive = false
        this.leftTopActive = true
      }
    }
  }
}
</script>

